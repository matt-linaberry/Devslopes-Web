var calculator = {
  add: function(var1, var2) {
    return var1 + var2;
  },
  subtract: function(var1, var2) {
    return var1 - var2;
  },
  multiply: function(var1, var2) {
    return var1 * var2;
  },
  divide: function(var1, var2) {
    return var1 / var2;
  }
};

module.exports = calculator;
